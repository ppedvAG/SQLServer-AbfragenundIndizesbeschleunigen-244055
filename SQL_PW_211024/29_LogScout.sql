/*
	SQL LogScout ist ein Tool von Microsoft, das zur Diagnose und Analyse von SQL Server-Problemen entwickelt wurde.

	- Hilft dabei Protokolle und Diagnosedaten von SQL Server-Instanzen zu sammeln
		=> Gut für Datenbankadministratoren & Support-Teams

	Wichtige Funktionen von SQL LogScout:

	1. Automatisierte Datensammlung:
	- Sammelt automtischt Protokolle, ohne das der Administrator das ganze Zeit überprüfen muss

	2. Leistungsanalyse:
	- Erfasst Leistungsdaten, keine Engpässe enstehen, um Leistungsprobleme zu identifizeren

	3. Flexible Protokollerstellung:
	 - gezielte Analyse durch bestimmte Konfiguration von Protokollen oder Diagnosedaten

	4. Einfacher Export
	- gesammelte Daten weiterleiten => Microsoft-Support
	ODER
	- gesammelte Daten weiterleiten => Analyse Tools

	5. Reduzierte Fehleranalysezeit:
	- Durch die Automatisierung der Datensammlung kann SQL LogScout die Zeit für Problemidentifikation reduzieren

	Anwendungsfälle:
	- SQL Server instanz wird langsamer
	- Fehlermeldungen angezeigt werden
	- Verbindungsprobleme
*/