/*
	SQLNexus ist ein Diagnosetool, das verwendet wird um Leistungsprobleme in
	SQL Server-Umgebungen zu analysieren und zu beheben
	=> Importiert und verarbeitet Daten, die mithilfe andere Tools wie SQL Sever Profiler
	gesammelt wurden und erstellt dann visuelle Berichte zur Problemursache

	Funktionen vom SQL Nexus:

	1. Datenimport aus verschiedenen Quellen wie z. B SQL Server Profiler

	2. Analyse und Berichterstellung:
	- detaillierte Berichte über CPU, Speicher, oder Festplatten & gibt Einblicke in die SQL Sever Abfrageleistung

	3. Visualisierung
	- Stellt Daten visuell dar, die Berichte umfassen Diagramme und Tabellen

	4. Automatisierte Leistungsanalyse: Analyisiert, behebt das Problem

	
	Einsatzgebiete von SQLNexus:
	- Optimierungen von langsamen Abfragen, datenbankoperatioen etc...

*/